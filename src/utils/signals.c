#include "minishell.h"

void	sigint_handler(int signum)
{
	(void) signum;
	write(STDOUT_FILENO, "\n", 1);
	rl_replace_line("", 0);
	rl_on_new_line();
	rl_redisplay();
	g_sig_received = 2;
}

void	func_int(int signum)
{
	(void)signum;
	write(1, "\n", 1);
}
